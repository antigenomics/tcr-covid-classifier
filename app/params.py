abstract_text = '''
**<font size=5>Abstract</font>**\n
The COVID-19 pandemic clearly demonstrates the need to monitor the spread of infectious diseases and population immunity. 
Probing adaptive immunity by sequencing the repertoire of antigen receptors (Rep-Seq) encoding specificity and 
immunological memory has become a method of choice for immunology studies. Rep-Seq can detect the imprint of past and ongoing 
infections and study individual responses to SARS-CoV-2 as shown in a number of recent studies. In present study we apply a machine
learning approach to two large datasets with hundreds of pre-COVID-19 and convalescent donor repertoires to infer 
T-cell receptor (TCR) repertoire features that were induced by SARS-CoV-2 exposure. Proper standardization of Rep-Seq batches, 
access to human leukocyte antigen (HLA) typing and both alpha- and beta-chain sequences of TCRs allowed us to generate a high-quality 
biomarker database and build a robust and highly accurate classifier for COVID-19 exposure applicable to individual TCR repertoires 
obtained using different protocols, paving a way to Rep-Seq-based immune status assessment in large cohorts of donors.'''
